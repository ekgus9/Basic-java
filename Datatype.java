public class Datatype{
	public static void main (String[] args) {
		
		
		System.out.println(); // /sout/ + ctrl +space
		
		
		// 1. number
		
		System.out.println(6/2); // operator 연산자
		
		System.out.println(Math.floor(Math.PI)); // up
		System.out.println(Math.ceil(Math.PI)); // down
		
		
		// 2. string
		
		System.out.println('h'); // character : 한 글자를 표현하는 데이터 타입
		
		System.out.println("6"+"6"); // string combination
		
		System.out.println("1111".length()); // 4
		// System.out.println(1111.length()); // error
		
		System.out.println("hello \nworld"); // new line
		System.out.println("hello \"world\""); // escape
		
		System.out.println("Hello!! My name is ...".replace("...", "ekgus9"));
		
	} 
}